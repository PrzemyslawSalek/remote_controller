package com.app.remote_controller_app.lists.elements;

public class ComponentListElement {

    int image;
    String name;

    public ComponentListElement(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
